fn main() {
    // Addition operation
    let sum = 5 + 10;
    println!("Addition: {sum}");

    // Subtraction operation
    let difference = 15 - 5;
    println!("Subtraction: {difference}");

    // Multiplication operation
    let product = 4 * 3;
    println!("Multiplication: {product}");

    // Division operation
    let quotient = 20.78 / 4.22;
    println!("Division: {quotient}");
    let truncated_quotient = 22 / 4; // Integer division
    println!("Truncated Division: {truncated_quotient}");

    // Remainder operation
    let remainder = 22 % 4;
    println!("Remainder: {remainder}");
}
