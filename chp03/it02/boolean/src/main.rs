fn main() {
    let t = true; // Boolean value, can be either true or false
    let f = false; // Boolean value, can be either true or false
    println!("Boolean values:");
    println!("\t - True: {t}, \n\
              \t - False: {f}" 
            );
    // Explicitly typed boolean values
    let t: bool = true; // Explicitly typed boolean value
    let f: bool = false; // Explicitly typed boolean value
    println!("Explicitly typed boolean values:");
    println!("\t - True: {t}, \n\
              \t - False: {f}" 
            );
}
